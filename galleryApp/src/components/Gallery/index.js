class Display extends Component {
  state = {display: imagesList[0].id}

  change = id => {
    this.setState({
      display: id,
    })
  }

  render() {
    const {display} = this.state
    const Image = imagesList.filter(eachItem => eachItem.id === display)

    console.log(Image)

    return (
      <div className="container">
        <img src={Image[0].imageUrl} alt={Image[0].imageAltText} />
        <h1>Nature Photography</h1>
        <p>Nature Photography by Rahul</p>
        <ul className="list">
          {imagesList.map(eachItem => {
            let cla

            if (display !== eachItem.id) {
              cla = 'image-blur'
            }

            return (
              <Photo
                change={this.change}
                key={eachItem.id}
                imgDetails={eachItem}
                clas={cla}
              />
            )
          })}
        </ul>
      </div>
    )
  }
}

export default Display
